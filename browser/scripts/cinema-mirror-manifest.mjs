// Shared definition of WHAT the extension mirrors from @infrix/cinema-core and
// WHERE the canonical source lives. Imported by both the sync script
// (scripts/sync-cinema-core.mjs) and the drift fence
// (tests/cinema_core_mirror.test.mjs) so the two can never disagree about the
// file set — the stale-hardcoded-list class of bug is gone.
//
// The mirror is DERIVED, not enumerated: every browser-mountable core asset in
// the canonical package is mirrored automatically. To deliberately keep a file
// out of the extension, add it to EXCLUDE — never by silently omitting it from
// a list.

import { existsSync } from 'node:fs';
import { readdir, readFile, stat } from 'node:fs/promises';
import { createRequire } from 'node:module';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const require = createRequire(import.meta.url);
const here = path.dirname(fileURLToPath(import.meta.url));

// browser/ — the extension root (scripts/ lives directly under it).
export const extensionRoot = path.dirname(here);

// browser/cinema-core/ — the generated mirror directory. This directory is
// build output: it is fully regenerated by the sync script and pruned to match
// canonical. Never hand-edit it, and never add hand-maintained files here (they
// would be pruned) — put docs in browser/, not in the mirror.
export const mirrorDir = path.join(extensionRoot, 'cinema-core');

// Files that exist in the canonical package but are deliberately NOT mirrored.
// loader.js is the ESM entry point and scriptManifest.js is the ESM load-order
// source of truth (cinema-core DX P3-5); the extension loads the classic IIFE
// scripts directly via <script> tags, so neither ESM file has a place in the
// bundle.
export const EXCLUDE = new Set(['loader.js', 'scriptManifest.js']);

// isMountable reports whether a canonical filename is a browser-mountable core
// asset the extension should ship: a classic .js script or a .css file, minus
// the deliberate exclusions and unit tests. Non-asset files (README.md,
// demo.html, the docs/ directory, *.test.mjs) are ignored.
export function isMountable(file) {
  if (EXCLUDE.has(file)) return false;
  if (/\.test\.[mc]?js$/.test(file)) return false; // unit tests stay in canonical
  return /\.(js|css)$/.test(file);
}

// normalizeLF collapses CRLF to LF. The mirror is committed and compared in LF
// regardless of the developer's core.autocrlf or platform, so the byte-identity
// guarantee holds the same on Windows and Linux. The .gitattributes pins these
// assets to eol=lf so the working tree matches what the tooling writes (no
// perpetual "modified" churn).
export function normalizeLF(text) {
  return (Buffer.isBuffer(text) ? text.toString('utf8') : String(text)).replace(/\r\n/g, '\n');
}

// isCoreAsset reports whether a filename in the mirror directory is something
// this tooling owns (a .js/.css asset). Used by the prune step to decide what
// it is allowed to delete: it removes only core assets that canonical no longer
// has, never unrelated files. loader.js counts (so a stray ESM loader that
// somehow lands in the mirror gets pruned), while it is excluded from the set
// the sync writes.
export function isCoreAsset(file) {
  return /\.(js|css)$/.test(file);
}

// resolveSrc locates the canonical @infrix/cinema-core source, local-first, so a
// developer editing the sibling working copy can mirror immediately without
// publishing or installing anything. Resolution order:
//
//   1. INFRIX_CINEMA_CORE_SRC  — explicit override (CI checks the canonical repo
//                                out at a pinned ref and points here; keeps
//                                enforcement publish-independent).
//   2. installed @infrix/cinema-core — the published package (post-publish).
//   3. ../../infrix-cinema-core — the sibling working copy (the common local
//                                monorepo-of-repos layout under Infrix/).
//
// Returns { dir, via }. Throws if none resolve — we never silently fall back to
// the (possibly stale) committed mirror.
export function resolveSrc() {
  const env = process.env.INFRIX_CINEMA_CORE_SRC;
  if (env) {
    const p = path.resolve(env);
    if (!existsSync(path.join(p, 'package.json'))) {
      throw new Error(`INFRIX_CINEMA_CORE_SRC=${env} does not contain a package.json (resolved to ${p})`);
    }
    return { dir: p, via: 'INFRIX_CINEMA_CORE_SRC' };
  }
  try {
    return { dir: path.dirname(require.resolve('@infrix/cinema-core/package.json')), via: 'installed @infrix/cinema-core' };
  } catch {
    /* not installed — try the sibling working copy */
  }
  const sibling = path.resolve(extensionRoot, '../../infrix-cinema-core');
  if (existsSync(path.join(sibling, 'package.json'))) {
    return { dir: sibling, via: 'sibling ../../infrix-cinema-core' };
  }
  throw new Error(
    'cannot locate @infrix/cinema-core. Set INFRIX_CINEMA_CORE_SRC to its path, ' +
    'install it (npm install), or place the infrix-cinema-core repo as a sibling of infrix-extensions.',
  );
}

// tryResolveSrc is the non-throwing variant for the fence, which SKIPS (rather
// than fails) when no canonical source is available — the committed mirror still
// ships and the popup tests exercise it. Returns null when unresolved.
export function tryResolveSrc() {
  try { return resolveSrc(); } catch { return null; }
}

// resolveSrcForCheck is the resolution policy for the enforcement gate (fence +
// vendor:check). It closes a footgun: if INFRIX_CINEMA_CORE_SRC is EXPLICITLY
// set, resolution MUST succeed — a typo'd path fails loudly (throws) rather than
// silently skipping the gate. Only when no source is requested AND none is
// discoverable does it return null (skip). This guarantees CI, which sets the
// env var, can never pass by accidentally skipping.
export function resolveSrcForCheck() {
  if (process.env.INFRIX_CINEMA_CORE_SRC) return resolveSrc(); // throws on bad path
  return tryResolveSrc();
}

// listMountable returns the sorted set of mountable filenames in a directory —
// the canonical list of what the mirror should contain.
export async function listMountable(dir) {
  const entries = await readdir(dir);
  return entries.filter(isMountable).sort();
}

// computeMirrorPlan compares the canonical source against the mirror directory
// and returns the categorized set of changes the sync would apply. This is the
// SINGLE comparison implementation shared by the sync script (to drive
// writes/prunes and report) and the drift check (to pass/fail). Byte-level: a
// file counts as `update` only when its bytes differ.
//
//   { add: [], update: [], unchanged: [], remove: [], want: [] }
//
// - add/update/unchanged: mountable canonical files keyed by mirror state
// - remove: core assets present in the mirror that canonical no longer has
// - want: the full sorted set of files the mirror should contain
export async function computeMirrorPlan(src, dest) {
  const want = await listMountable(src);
  const wantSet = new Set(want);
  const plan = { add: [], update: [], unchanged: [], remove: [], want };

  for (const f of want) {
    const body = normalizeLF(await readFile(path.join(src, f)));
    const destPath = path.join(dest, f);
    const prev = existsSync(destPath) ? normalizeLF(await readFile(destPath)) : null;
    if (prev === null) plan.add.push(f);
    else if (prev === body) plan.unchanged.push(f);
    else plan.update.push(f);
  }

  if (existsSync(dest)) {
    for (const entry of await readdir(dest)) {
      if (!isCoreAsset(entry)) continue;
      if (wantSet.has(entry)) continue;
      const st = await stat(path.join(dest, entry));
      if (st.isFile()) plan.remove.push(entry);
    }
  }
  plan.remove.sort();
  return plan;
}

// planHasDrift reports whether a plan represents any divergence between mirror
// and canonical (anything to add, update, or remove).
export function planHasDrift(plan) {
  return plan.add.length > 0 || plan.update.length > 0 || plan.remove.length > 0;
}

// The HTML comment markers that bracket the generated cinema-core <link>/<script>
// block in popup.html. Everything between them is regenerated by the sync from
// the canonical loader order; everything outside (the extension's own scripts)
// is hand-authored and left untouched.
export const POPUP_BLOCK_BEGIN = '<!-- cinema-core:begin — GENERATED by scripts/sync-cinema-core.mjs from the @infrix/cinema-core loader order. Do not edit by hand; run: npm run vendor -->';
export const POPUP_BLOCK_END = '<!-- cinema-core:end -->';

// The HTML host pages that load the mirrored core. Each carries the
// cinema-core:begin/end markers and is regenerated from the canonical loader
// order, so neither can fall behind. Both live one directory under browser/
// (popup/ and cinema/), so both reference the core at '../cinema-core'.
export const popupHtmlPath = path.join(extensionRoot, 'popup', 'popup.html');
export const consoleHtmlPath = path.join(extensionRoot, 'cinema', 'console.html');

// cinemaHtmlTargets returns the existing host pages whose load block the sync
// owns. Filtered to files that exist so the tooling never errors on an
// optional page that has not been created yet.
export function cinemaHtmlTargets() {
  return [popupHtmlPath, consoleHtmlPath].filter((p) => existsSync(p));
}

// parseLoaderOrder reads the canonical loader.js and extracts the authoritative
// load order — the SCRIPTS array (classic scripts in dependency order, app.js
// last) and the STYLES array. This is the single source of truth for HOW the
// extension must load the core, so popup.html never has to guess an order.
//
// It also validates that the loader's union of scripts+styles is EXACTLY the
// mountable file set: if canonical adds a module to the package but forgets to
// register it in loader.js (or vice-versa), that is a canonical defect and we
// surface it here rather than shipping a module the extension never loads.
export async function parseLoaderOrder(srcDir) {
  // The authoritative load order moved to scriptManifest.js (CINEMA_SCRIPTS /
  // CINEMA_STYLES) as of cinema-core DX P3-5; older canonical versions kept the
  // SCRIPTS / STYLES arrays inline in loader.js. Support both so this fence works
  // across a canonical version bump.
  const manifestPath = path.join(srcDir, 'scriptManifest.js');
  const useManifest = existsSync(manifestPath);
  const sourceFile = useManifest ? 'scriptManifest.js' : 'loader.js';
  const text = await readFile(path.join(srcDir, sourceFile), 'utf8');
  const grab = (name) => {
    const m = text.match(new RegExp(`const ${name}\\s*=\\s*\\[([\\s\\S]*?)\\]`));
    if (!m) throw new Error(`${sourceFile}: could not find the ${name} array`);
    return [...m[1].matchAll(/['"]([^'"]+)['"]/g)].map((x) => x[1]);
  };
  const scripts = grab(useManifest ? 'CINEMA_SCRIPTS' : 'SCRIPTS');
  const styles = grab(useManifest ? 'CINEMA_STYLES' : 'STYLES');

  const fromLoader = [...scripts, ...styles].sort();
  const mountable = await listMountable(srcDir);
  const loaderSet = new Set(fromLoader);
  const mountSet = new Set(mountable);
  const missingFromLoader = mountable.filter((f) => !loaderSet.has(f));
  const extraInLoader = fromLoader.filter((f) => !mountSet.has(f));
  if (missingFromLoader.length || extraInLoader.length) {
    throw new Error(
      `${sourceFile} order disagrees with the mountable file set — canonical defect.\n` +
      (missingFromLoader.length ? `  mountable but not in ${sourceFile}: ${missingFromLoader.join(', ')}\n` : '') +
      (extraInLoader.length ? `  in ${sourceFile} but not mountable: ${extraInLoader.join(', ')}\n` : ''),
    );
  }
  return { scripts, styles };
}

// renderPopupCinemaBlock produces the exact text (including the begin/end
// markers) that must live in popup.html for a given loader order. Both the sync
// (to write it) and the fence (to compare) call this — one definition of "the
// correct block".
export function renderPopupCinemaBlock({ scripts, styles }, { base = '../cinema-core', indent = '' } = {}) {
  const lines = [`${indent}${POPUP_BLOCK_BEGIN}`];
  for (const s of styles) lines.push(`${indent}<link rel="stylesheet" href="${base}/${s}">`);
  for (const s of scripts) lines.push(`${indent}<script src="${base}/${s}"></script>`);
  lines.push(`${indent}${POPUP_BLOCK_END}`);
  return lines.join('\n');
}

// replacePopupCinemaBlock returns popup.html with the region between the markers
// replaced by `block`. Throws if the markers are absent or malformed so the
// generator never silently no-ops.
export function replacePopupCinemaBlock(html, block) {
  const begin = html.indexOf(POPUP_BLOCK_BEGIN);
  const end = html.indexOf(POPUP_BLOCK_END);
  if (begin === -1 || end === -1 || end < begin) {
    throw new Error('popup.html is missing the cinema-core:begin/end markers — cannot regenerate the load block');
  }
  return html.slice(0, begin) + block.trimStart() + html.slice(end + POPUP_BLOCK_END.length);
}

// htmlBlocksStatus reports whether EVERY cinema host page's generated load
// block already matches the canonical loader order. `current: false` means at
// least one page would change on the next `npm run vendor` — i.e. it loads the
// wrong set/order of cinema-core scripts. Shared by the sync, the check, and
// the fence so all agree on "the host pages are current".
export async function htmlBlocksStatus(srcDir) {
  const order = await parseLoaderOrder(srcDir);
  const block = renderPopupCinemaBlock(order);
  const targets = cinemaHtmlTargets();
  const stale = [];
  for (const t of targets) {
    const html = normalizeLF(await readFile(t));
    if (replacePopupCinemaBlock(html, block) !== html) stale.push(path.basename(t));
  }
  return { current: stale.length === 0, stale, targets: targets.length, scripts: order.scripts.length, styles: order.styles.length };
}
